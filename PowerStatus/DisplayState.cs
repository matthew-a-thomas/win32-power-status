namespace PowerStatus;

/// <summary>
/// Various display states.
/// </summary>
public enum DisplayState
{
    /// <summary>
    /// The display is off.
    /// </summary>
    Off = 0,
    /// <summary>
    /// The display is on.
    /// </summary>
    On = 1,
    /// <summary>
    /// The display is dimmed.
    /// </summary>
    Dimmed = 2
}